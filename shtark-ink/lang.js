  function _norm_dummy() {}

  // Active normalized lookup (rebuilt per language)
  var LOOKUP = {};
  function buildLookup(dict) {
    LOOKUP = {};
    Object.keys(dict).forEach(function (k) { LOOKUP[norm(k)] = dict[k]; });
  }

  // Reverse maps: normalized(translated value) -> russian source.
  // Lets restore() revert nodes even when their origText entry was lost
  // (e.g. React replaced the node while a non-RU language was active).
  var REVERSE = {};
  function buildReverse() {
    REVERSE = {};
    [DICT_EN, (typeof DICT_KK !== 'undefined' ? DICT_KK : {})].forEach(function (dict) {
      Object.keys(dict).forEach(function (ruKey) {
        var val = dict[ruKey];
        if (val == null) return;
        var nv = norm(val);
        if (nv && REVERSE[nv] == null) REVERSE[nv] = ruKey;
      });
    });
  }

  var SKIP = { SCRIPT: 1, STYLE: 1, NOSCRIPT: 1, svg: 1, SVG: 1 };
  var origText = new WeakMap();   // textNode -> original value
  var origAttr = new WeakMap();   // element -> {placeholder, value}

  function translateTextNodes(root) {
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode: function (n) {
        var p = n.parentNode;
        if (!p) return NodeFilter.FILTER_REJECT;
        var tag = p.nodeName;
        if (SKIP[tag]) return NodeFilter.FILTER_REJECT;
        if (p.closest && p.closest('.hdr__lang')) return NodeFilter.FILTER_REJECT;
        if (!n.nodeValue || !n.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    var nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(function (n) {
      var key = norm(n.nodeValue);
      if (LOOKUP[key] != null) {
        if (!origText.has(n)) origText.set(n, n.nodeValue);
        // preserve trailing arrow/space cosmetics already in dict value
        n.nodeValue = LOOKUP[key];
      }
    });
  }

  function translateAttrs(root) {
    var els = root.querySelectorAll('input[placeholder], textarea[placeholder], input[value]');
    els.forEach(function (el) {
      var saved = origAttr.get(el) || {};
      var ph = el.getAttribute('placeholder');
      if (ph && LOOKUP[norm(ph)] != null) {
        if (saved.placeholder == null) saved.placeholder = ph;
        el.setAttribute('placeholder', LOOKUP[norm(ph)]);
      }
      origAttr.set(el, saved);
    });
  }

  function restore() {
    // text
    var all = document.querySelectorAll('body *');
    // restore text nodes
    var walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null);
    var nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(function (n) {
      if (origText.has(n)) { n.nodeValue = origText.get(n); return; }
      // Fallback: node lost its origText entry (React re-render) — reverse-map
      // any translated value back to its Russian source.
      var key = norm(n.nodeValue);
      if (REVERSE[key] != null) n.nodeValue = REVERSE[key];
    });
    // attrs
    document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(function (el) {
      var saved = origAttr.get(el);
      if (saved && saved.placeholder != null) el.setAttribute('placeholder', saved.placeholder);
    });
  }

  var current = 'ru';
  var observer = null;

  function applyLang() {
    translateTextNodes(document.body);
    translateAttrs(document.body);
    if (!observer) {
      var pending = false;
      observer = new MutationObserver(function (muts) {
        if (current === 'ru' || pending) return;
        pending = true;
        requestAnimationFrame(function () {
          pending = false;
          translateTextNodes(document.body);
          translateAttrs(document.body);
        });
      });
      observer.observe(document.body, { childList: true, subtree: true, characterData: true });
    }
    document.documentElement.lang = current;
  }

  var DICTS = { en: DICT_EN, kk: (typeof DICT_KK !== 'undefined' ? DICT_KK : {}), zh: (typeof DICT_ZH !== 'undefined' ? DICT_ZH : {}) };

  function setLang(lang) {
    if (lang !== 'en' && lang !== 'kk' && lang !== 'zh') lang = 'ru';
    current = lang;
    try { localStorage.setItem('si-lang', lang); } catch (e) {}
    if (lang === 'ru') {
      restore();
      document.documentElement.lang = 'ru';
    } else {
      restore();
      var __merged = (lang === 'en') ? DICT_EN : Object.assign({}, DICT_EN, DICTS[lang] || {});
      buildLookup(__merged);
      applyLang();
    }
    document.querySelectorAll('.hdr__lang-select').forEach(function (sel) {
      if (sel.value !== lang) sel.value = lang;
    });
  }

  window.__setLang = setLang;

  // User-triggered switch: persist choice and reload so the whole page re-renders in the new language.
  window.__switchLang = function (lang) {
    if (lang !== 'en' && lang !== 'kk' && lang !== 'zh') lang = 'ru';
    try { localStorage.setItem('si-lang', lang); } catch (e) {}
    location.reload();
  };

